      class="add-update-preview"
      ref="ruleForm"
      :model="ruleForm"
      :rules="rules"
      label-width="80px"
    >
          <el-form-item :style='{"width":"49%","padding":"10px","margin":"0 0 10px","background":"none"}' label="课程名称" prop="kechengmingcheng">
            <el-input v-model="ruleForm.kechengmingcheng" 
                placeholder="课程名称" clearable :disabled=" false  ||ro.kechengmingcheng"></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"49%","padding":"10px","margin":"0 0 10px","background":"none"}' label="作业名称" prop="zuoyemingcheng">
            <el-input v-model="ruleForm.zuoyemingcheng" 
                placeholder="作业名称" clearable :disabled=" false  ||ro.zuoyemingcheng"></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"49%","padding":"10px","margin":"0 0 10px","background":"none"}' label="教师工号" prop="jiaoshigonghao">
            <el-input v-model="ruleForm.jiaoshigonghao" 
                placeholder="教师工号" clearable :disabled=" false  ||ro.jiaoshigonghao"></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"49%","padding":"10px","margin":"0 0 10px","background":"none"}' label="教师姓名" prop="jiaoshixingming">
            <el-input v-model="ruleForm.jiaoshixingming" 
                placeholder="教师姓名" clearable :disabled=" false  ||ro.jiaoshixingming"></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"49%","padding":"10px","margin":"0 0 10px","background":"none"}' label="学生账号" prop="xueshengzhanghao">
            <el-input v-model="ruleForm.xueshengzhanghao" 
                placeholder="学生账号" clearable :disabled=" false  ||ro.xueshengzhanghao"></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"49%","padding":"10px","margin":"0 0 10px","background":"none"}' label="学生姓名" prop="xueshengxingming">
            <el-input v-model="ruleForm.xueshengxingming" 
                placeholder="学生姓名" clearable :disabled=" false  ||ro.xueshengxingming"></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"49%","padding":"10px","margin":"0 0 10px","background":"none"}' label="作业评分" prop="zuoyepingfen">
            <el-input v-model="ruleForm.zuoyepingfen" 
                placeholder="作业评分" clearable :disabled=" false  ||ro.zuoyepingfen"></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"49%","padding":"10px","margin":"0 0 10px","background":"none"}' label="批改时间" prop="pigaishijian">
              <el-date-picker
				  :disabled=" false  ||ro.pigaishijian"
                  value-format="yyyy-MM-dd HH:mm:ss"
                  v-model="ruleForm.pigaishijian" 
                  type="datetime"
                  placeholder="批改时间">
              </el-date-picker>
          </el-form-item>
          <el-form-item :style='{"width":"49%","padding":"10px","margin":"0 0 10px","background":"none"}' label="教师评语" prop="jiaoshipingyu">
            <el-input
              type="textarea"
              :rows="8"
              placeholder="教师评语"
              v-model="ruleForm.jiaoshipingyu">
            </el-input>
          </el-form-item>

      <el-form-item :style='{"width":"100%","padding":"0","margin":"0","textAlign":"center"}'>
        <el-button :style='{"border":"0","cursor":"pointer","padding":"0 10px","margin":"0 20px 0 0","outline":"none","color":"rgba(255, 255, 255, 1)","borderRadius":"4px","background":"#FE6917","width":"auto","lineHeight":"30px","fontSize":"14px","height":"30px"}'  type="primary" @click="onSubmit">提交</el-button>
        <el-button :style='{"border":"0","cursor":"pointer","padding":"0 10px","margin":"0","outline":"none","color":"#fff","borderRadius":"5px","background":"#9E9E9E","width":"auto","lineHeight":"30px","fontSize":"14px","height":"30px"}' @click="back()">返回</el-button>
